// 根据店铺名称生成唯一 shopId
export const getShopIdByName = (shopName: string): string => {
  // 使用简单的 hash 函数生成唯一 ID
  let hash = 0;
  for (let i = 0; i < shopName.length; i++) {
    const char = shopName.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  return `shop-${Math.abs(hash)}`;
};

